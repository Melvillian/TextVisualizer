public class PosNegVal {
    Double pos;
    Double neg;

    PosNegVal(Double pos, Double neg) {
        this.pos = pos;
        this.neg = neg;
    }

    public Double getNeg() {
        return neg;
    }

    public Double getPos() {

        return pos;
    }
}
